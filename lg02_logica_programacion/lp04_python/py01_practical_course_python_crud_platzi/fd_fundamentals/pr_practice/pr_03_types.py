# Cadenas    : <class 'str'>
print("\nCadenas    : <class 'str'>")
my_name = 'Albeiro'
print("     my_name = 'Albeiro'")
print("     print(my_name) =", my_name)
print("     type(my_name) =", type(my_name))

# Enteros   : <class 'int'>
print("\nEnteros    : <class 'int'>")
my_age = 40
print("     my_age = 40")
print("     print(my_age) =", my_age)
print("     type(my_age) =", type(my_age))

# Booleanos   : <class 'bool'>
print("\nBooleanos    : <class 'bool'>")
is_single = True
print("     is_single = True")
print("     print(is_single) =", is_single)
print("     type(is_single) =", type(is_single))

# Listas   : <class 'list'>
print("\nListas    : <class 'list'>")
list = ['Hola', 1, True]
print("     list = ['Hola', 1, True]")
print("     print(list) =", list)
print("     print(list[0]) =", list[0])
print("     type(list) =", type(list))

# Diccionarios   : <class 'dict'>
print("\nDiccionarios    : <class 'dict'>")
rae = {'pizza': 'Comida rápida triangular', 'pasta': 'Comida popular italiana'}
print("     rae = {'pizza': 'Comida rápida triangular', 'pasta': 'Comida popular italiana'}")
print("     print(rae) =", rae)
print("     print(rae.keys()) =", rae.keys())
print("     print(rae.values()) =", rae.values())
print("     print(rae.items()) =", rae.items())
print("     type(rae) =", type(rae))

# Tuplas   : <class 'tuple'>
print("\nTuplas    : <class 'tuple'>")
a = 1, 2, 3
b = (4, 5, 6)
print("     a = 1, 2, 3")
print("     b = (4, 5, 6)")
print("     print(a) =", a)
print("     print(b) =", b)
print("     print(type(a)) =", type(a))
print("     print(type(b)) =", type(a))
